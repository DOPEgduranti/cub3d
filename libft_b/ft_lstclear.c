#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*tofree_lst;

	if (!lst || !*lst || !del)
		return ;
	while (*lst)
	{
		del((*lst)->content);
		tofree_lst = *lst;
		*lst = (*lst)->next;
		free(tofree_lst);
	}
	lst = NULL;
}
