#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*dst;
	size_t			l_s;

	i = 0;
	l_s = 0;
	if (s == NULL)
		return (NULL);
	while (s[l_s])
		l_s++;
	dst = (char *)malloc((l_s + 1) * sizeof(char));
	if (dst == NULL)
		return (NULL);
	while (s[i])
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
