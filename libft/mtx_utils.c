#include "libft.h"

void	ft_freemtx(char ***mtx)
{
	size_t	i;

	i = 0;
	while ((*mtx)[i])
	{
		free((*mtx)[i]);
		i++;
	}
	free(*mtx);
	mtx = NULL;
}